

def piling_up(values):
    i = 0
    j = len(values) - 1
    current_max = max(values[i], values[j])
    while i != j:
        iv = values[i]
        jv = values[j]
        if current_max < max(values[i], values[j]):
            print('No')
            break
        if iv >= jv:
            i += 1
        else:
            j -= 1
    else:
        print('Yes')


